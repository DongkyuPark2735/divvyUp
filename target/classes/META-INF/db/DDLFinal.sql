CREATE TABLE GROUPS(
    GID NUMBER(6) PRIMARY KEY,
    GNAME VARCHAR2(100),
    GRDATE DATE DEFAULT SYSDATE,
    GIMG VARCHAR2(100),
    GCONTENT VARCHAR2(4000),
    MID VARCHAR2(100) REFERENCES MEMBER(MID)); -- �׷� ���� ����� ID
  
